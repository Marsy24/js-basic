const lang = 'EN';

switch (lang) {
    case 'EN':
        console.log('Hello!');
        break;

    case 'FR':
        console.log('Bonjour!');
        break;

    case 'RU':
        console.log('Привет!');
        break;

    default:
        console.log('Language not supported');
        break;
}